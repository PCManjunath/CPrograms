#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("Hello world!\n");
    printf("%d \n", 1992); /// Holds the Integer value.
    printf("%f \n", 32.2); /// Holds decimal value FLOAT value.
    printf("%lf \n", 123456.987); /// Holds large float value.
    printf("%c \n", 'M'); /// Holds Char - Single character.
    printf("%s \n", "Hello Word"); /// Similar to Printf, holds STRING value.
    printf("%x \n", 16); /// Hexa decimal value.
    return 0;
}
